class BasicArithmetic{
    public static void main (String[] args){
     
        float num1 = 5;
        float num2 = 8;

        float add = num1 + num2;
        float sub = num1 - num2;
        float multi = num1 * num2;
        float div = num1 / num2;
        float mod = num2 % num1;


        System.out.println(add);
        System.out.println(sub);
        System.out.println(multi);
        System.out.println(div);
        System.out.println(mod);
    }
}